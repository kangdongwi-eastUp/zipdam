3. 외부 저장소: 구매 상품에 대한 리뷰 및 게시글 작성 시 파일 첨부(Android)
2. 필수 접근항목은 앱설치시 또는 최초 실행시 안내하며, 선택 접근항목은 해당 서비스 최초 이용시 별도의 동의를 받습니다. 선택 접근항목의 경우 운영체제의 버전에 따라 동의를 받는 방법이 다를 수 있으나, 접근을 거부하더라도 기본 서비스 이용에 제한이 없습니다.

