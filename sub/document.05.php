제8조 개인정보 보유 및 이용기간

1. 회사는 법령에 따른 개인정보 보유·이용기간 또는 이용자로부터 개인정보 수집시 동의받은 개인정보 보유·이용 기간 내에서 개인정보를 처리·보유합니다. 회사는 이용자가 입력한 정보 중 당사의 서비스 제공 과정에서 부정확한 것으로 확인된 정보(연락처, 배송지)에 관하여, 제3자의 권리와 이익의 침해를 방지하고 정보의 최신성 유지를 위하여 해당 정보를 즉시 파기할 수 있습니다.
2. 회사의 개인정보 처리 및 보유기간은 다음과 같습니다.
1.  회원가입 및 관리 : 회원탈퇴시까지. 다만, 다음 사유에 해당하는 경우에는 해당 사유 종료시까지
