3. 회사는 개인정보보호법 제21조, 제39조의6에 따라 장기간(1년 이상) 서비스 미이용자의 개인정보 보호를 위하여 다른 이용자의 개인정보와 분리하여 별도로 보관합니다.

제9조 개인정보의 파기 절차 및 방법

1. 원칙적으로 이용자의 개인정보는 개인정보의 수집 및 이용목적의 결과물이 완성되면 지체 없이 파기됩니다.
2. 이용자로부터 동의를 받은 개인정보는 보유기간이 경과하거나 처리 목적이 완성되었음에도 불구하고 다른 법령에 따라 보관하는 경우에는, 해당 개인정보를 별도의 데이터베이스로 옮기거나 보관장소를 달리하여 보존합니다.
3. 개인정보의 파기 절차 및 방법은 다음과 같습니다.
1. 회사는 파기 사유가 발생한 개인정보를 선정하고, 회사의 개인정보 보호책임자의 승인을 받아 해당 개인정보를 파기합니다.
2. 회사는 종이에 기록, 저장된 개인정보는 분쇄기로 분쇄하거나 소각을 통하여 파기 하며 전자적 파일 형태로 저장된 개인정보: 기록을 재생할 수 없도록 로우레벨포맷등 기술적 방법을 사용하여 삭제합니다.


