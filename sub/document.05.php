<h3>(주)집담 개인정보 처리방침</h3>
<pre style=" white-space:pre-wrap;font-size:14px;font-weight:500;line-height:1.6;margin:60px 0;word-break: keep-all;">
(주)집담(이하 회사라 함)는 원활한 서비스 제공을 위해 최소한의 범위 내에서 아래와 같이 개인정보를 수집·이용하며 동의받은 목적 이외의 용도로 사용하지 않습니다.

제1조 개인정보의 처리목적

회사는 다음 각 호의 목적으로 이용자의 개인정보를 수집 및 이용합니다.
처리하고 있는 개인정보는 다음의 목적 이외의 용도로는 이용되지 않으며, 이용 목적이 변경되는 경우에는 별도의 동의를 받는 등 필요한 조치를 이행합니다.

1.회사에서 제공하는 서비스를 자유롭게 선택하고 서비스 신청을 함으로서 그 결과물의 제공에 필요한 정보 및 결제처리 정보,신청인을 대리하는 위임자의 정보
2.회원제 서비스 제공에 따른 개인식별, 가입의사 확인, 이용약관 위반 회원에 대한 이용제한 조치, 가입 및 가입횟수 제한, 서비스 부정 이용 제재, 비인가 사용 방지, 고충 처리 및 분쟁 조정을 위한 기록 보존, 고지사항 전달, 회원탈퇴 의사의 확인, 부정가입 및 이용 방지 등
3.신규 서비스 개발 및 맞춤 서비스 제공, 인구통계학적 특성에 따른 서비스 제공 및 광고 게재, 서비스의 유효성 확인, 이벤트·광고성 정보 및 참여 기회 제공, 접속빈도 파악, 회원의 서비스 이용에 대한 통계, 만14세 미만 아동의 정보수집 방지, 부정이용방지
4.민원인 신원 확인, 민원사항 확인, 사실조사를 위한 연락·통지, 처리결과 통보


제2조 처리하는 개인정보의 항목

회사는 다음 각 호의 개인정보를 처리하고 있습니다.

1. 웹또는 앱을 이용해 신청한 서비스의 결과물 이행을 위한 정보로서 이용자인 신청인이 대리인인경우 위임자의 성명,연락처,생년월일,신청인의 에 대한 정보 이메일, 휴대폰번호,사업자정보,서비스중 행위허가의 경우 사무처리 절차시 매매계약서 사본
2. 서비스의 제공 및 신규서비스 개발을 위한 나이, 방문일시, IP주소, 쿠키 등 서비스이용기록 및 기기정보
3. 고충처리 신청인 연락처,신청인 이름, 입금자이름
4. 이용자가 서비스를 구매한 경우 성명, 휴대전화번호, 이메일, 서비스 구매정보,카드 결제 정보,무통장 거래시(결재,환불) 계좌 정보, 현장 주소 정보
5. 서비스 처리결과의 최종 수혜자(입주자)가 신청인이 아닌 상대방으로서 신청을 대리하는 경우 소유주의 성명,연락처,생년월일(공동소유자포함,행위허가 의뢰시)
6. 회사는 만 14세 미만 아동의 개인정보를 수집하지 않습니다.

제3조 개인정보 수집방법

1. 회원가입 및 서비스 이용 과정에서 이용자가 개인정보 수집에 대해 동의하고 직접 정보를 입력하는 경우, 회사는 해당 정보를 수집합니다.
2. 고객센터를 통한 상담 과정에서 웹 또는 앱페이지, 메일, 팩스, 전화 등 다양한 경로를 통하여 회사에 상담·문의하는 경우, 개인정보가 수집될 수 있습니다.
3. 회사와 제휴한 외부 기업이나 단체로 부터 개인정보를 제공받을 수 있으며, 이러한 경우에는 제휴사에서 이용자에게 개인정보 제공 동의를 받아야 합니다.
4. 회사의 서비스 이용과정에서 로그분석을 통해 생성정보가 수집될 수 있으며, 쿠키에 의한 정보 등이 자동수집될 수 있습니다.

제4조 개인정보 자동수집 장치의 설치·운영 및 거부

1. 회사는 이용자에게 특화된 맞춤서비스를 제공하기 위해서 이용자들의 정보를 수시로 저장하고 찾아내는 '세션(session)' 또는 '쿠키(cookie)' 등을 운용합니다.
1. 세션 및 쿠키의 의미 : 세션이란 서비스를 제공하는데 이용되는 서버가 이용자의 접속 시간 동안 이용 정보를 서버에 저장하는 것을 의미하고, 쿠키란 웹 또는 앱을 운영하는 데 이용되는 서버가 이용자의 브라우저에 보내는 아주 작은 텍스트 파일로서 이용자의 컴퓨터 하드디스크에 저장되기도 하는 정보를 말합니다.
2. 세션 및 쿠키의 사용 목적 :회사는 세션 및 쿠키를 이용자의 접속 빈도나 방문 시간, 서비스이용 패턴을 분석하여 개인 맞춤 서비스를 제공하고 서비스 만족도를 높이기 위한 목적으로 사용합니다. 또한 각종 이벤트 참여 및 타겟 마케팅 등의 목적으로 쿠키 및 세션 정보를 사용합니다.
3. 세션 및 쿠키의 설정 거부 방법 : 이용자는 세션의 설치 및 설정에 대한 선택권을 가지지 않으나, 쿠키 설치 여부에 대한 선택권을 가지고 있습니다. 따라서, 이용자는 웹 브라우저에서 옵션을 설정함으로써 모든 쿠키를 허용하거나, 쿠키가 저장될 때마다 확인을 거치거나, 아니면 모든 쿠키의 저장을 거부할 수도 있습니다. 이용자는 사용하는 웹 브라우저의 옵션을 선택함으로써 모든 쿠키를 허용하거나 쿠키를 저장할 때마다 확인을 거치거나, 모든 쿠키의 저장을 거부할 수 있습니다.
- 인터넷 익스플로러의 경우, 웹 브라우저 상단의 [도구]>[인터넷 옵션]>[개인정보]>[고급] 메뉴를 통하여 쿠키 설정의 거부가 가능합니다.
- Safari의 경우, MacOS 상단 좌측 메뉴바에서 [Safari]>[환경설정]>[보안]을 통하여 쿠키 허용여부를 선택할 수 있습니다.
2. 이용자가 쿠키 설치를 거부하는 경우 로그인이 필요한 일부 서비스 이용에 어려움이 있을 수 있습니다.

제5조 접근권한에 대한 안내

